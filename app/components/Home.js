import React, { useState } from 'react';
import { StyleSheet, View, Text } from 'react-native';

const useFavoriteRecipes = () => {
  const [favoriteRecipes, setFavoriteRecipes] = useState([]);

  const addFavoriteRecipe = (recipe) => {
    setFavoriteRecipes([...favoriteRecipes, recipe]);
  };

  const removeFavoriteRecipe = (recipeId) => {
    setFavoriteRecipes(favoriteRecipes.filter((recipe) => recipe.id !== recipeId));
  };

  return {
    favoriteRecipes,
    addFavoriteRecipe,
    removeFavoriteRecipe,
  };
};

const Home = () => {
  const { favoriteRecipes } = useFavoriteRecipes();

  return (
    <View style={styles.container}>
      <Text style={styles.title}>Favorite Recipes</Text>
      {favoriteRecipes.length > 0 ? (
        favoriteRecipes.map(recipe => (
          <View key={recipe.id} style={styles.recipe}>
            <Text>{recipe.name}</Text>
            <Text>{recipe.description}</Text>
          </View>
        ))
      ) : (
        <Text style={styles.noFavorites}>You haven't added any favorite recipes yet.</Text>
      )}
    </View>
  );
};

const styles = StyleSheet.create({
  container: {
    flex: 1,
    backgroundColor: '#fff',
    paddingHorizontal: 16,
    paddingVertical: 24,
  },
  title: {
    fontSize: 24,
    fontWeight: 'bold',
    marginBottom: 16,
  },
  recipe: {
    marginBottom: 16,
  },
  noFavorites: {
    fontStyle: 'italic',
  },
});

export default Home;



